def addition(a: int, b: int) -> str:
    """Adds two numbers a and b and returns the result.
        Args:
            a (int): The first number to add.
            b (int): The second number to add.

        Returns:
            str: The sum of a and b.
    """ 
    return print(f"The sum of {a} and {b} is {a + b}")

def division(a: int, b: int) -> str:
    """Divides two numbers a and b and returns the result.
        Args:
            a (int): The first number to divide.
            b (int): The second number to divide.

        Returns:
            str: The division of a and b.
    """ 
    return print(f"The division of {a} and {b} is {a / b}")